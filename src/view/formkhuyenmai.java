        khuyenmai readForm() {
        String ngaybd,ngaykt,tenkm;
        int giatri;
        boolean trangthai;
            
        
         tenkm = txttenkm.getText().trim();
        if (tenkm.isEmpty()) {//mã rỗng
            JOptionPane.showMessageDialog(this, "chưa nhập họ tên km ");
            txttenkm.requestFocus();//chuyển chuột về test mã
            return null;//dừng chương trình

        }
         
        
        
        ngaybd = txtNgaybatdau.getText().trim();
        if (ngaybd.isEmpty()) {//mã rỗng
            JOptionPane.showMessageDialog(this, "chưa nhập họ ngày bắt đầu");
            txtNgaybatdau.requestFocus();//chuyển chuột về test mã
            return null;//dừng chương trình

        }
        
        ngaykt = txtNgayketthuc.getText().trim();
        if (ngaykt.isEmpty()) {//mã rỗng
            JOptionPane.showMessageDialog(this, "chưa nhập họ ngày kết thúc");
            txtNgayketthuc.requestFocus();//chuyển chuột về test mã
            return null;//dừng chương trình

        }
        
        if (txtgiatri.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "chưa nhập giá trị km");
            txtgiatri.requestFocus();
            return null;
        }
        String inputMakm = txtgiatri.getText().trim();
        if (!isNumeric(inputMakm)) {
            JOptionPane.showMessageDialog(this, "giá trị km phải là số");
            txtgiatri.requestFocus();//chuyển chuột về test mã
            return null;//dừng chương trình

        }try {
            giatri = Integer.parseInt(inputMakm);
            if (giatri <= 0) {
                JOptionPane.showMessageDialog(this, "giá trị km phải lớn hơn 0");
                txtgiatri.setText("");
                txtgiatri.requestFocus();
                return null;
            }
        } catch (NumberFormatException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "giá trị phải là số");
            txtgiatri.requestFocus();
            return null;
        }
         // Kiểm tra trạng thái từ combobox
    String selectedTrangThai = (String) cbbtrangthai.getSelectedItem();
    if (selectedTrangThai == null || selectedTrangThai.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Vui lòng chọn trạng thái");
        cbbtrangthai.requestFocus();
        return null;
    }
    // Xác định trạng thái dựa vào giá trị của combobox
    trangthai = selectedTrangThai.equals("Đang hoạt động");
    
    // Trả về một mảng các đối tượng chứa dữ liệu

    
         return new khuyenmai(giatri, tenkm, ngaybd, ngaykt, trangthai);
        }
    

