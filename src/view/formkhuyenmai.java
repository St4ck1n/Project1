    private void btnaddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnaddActionPerformed
        // TODO add your handling code here:
        if(this.readForm()!= null){//thêm được
            int chon = JOptionPane.showConfirmDialog(this, "Bạn có thêm không?");
            if(chon == 0){//có thêm
                kms.them(this.readForm()); // thêm vào database
                this.fillTable(kms.getAll());
                JOptionPane.showMessageDialog(this, "Thêm thành công");
                
            } else {
                JOptionPane.showMessageDialog(this, "Bạn không chọn thêm");
            }
        }else{
            JOptionPane.showMessageDialog(this, "Thêm thất bại");
        }
             
    }//GEN-LAST:event_btnaddActionPerformed

    private void btndeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btndeleteActionPerformed
        // TODO add your handling code here:
          if(this.readForm()!= null){//xóa được
            int chon = JOptionPane.showConfirmDialog(this, "Bạn có chắc chắn xóa không?");
            if(chon == 0){//có thêm
                kms.xoa(this.readForm());  
                model.removeRow(a);
                JOptionPane.showMessageDialog(this, "Xóa thành công");      
            } else {
                JOptionPane.showMessageDialog(this, "Bạn không chọn xóa");
            }
            }else{
                JOptionPane.showMessageDialog(this, "Xóa thất bại");
            }
    }//GEN-LAST:event_btndeleteActionPerformed

    private void btneditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneditActionPerformed
        // TODO add your handling code here:
          a = tblbangkhuyenmai.getSelectedRow();
        if (a >= 0) {
            khuyenmai km = readForm();
            if (km != null) {
                int manv = Integer.parseInt(tblbangkhuyenmai.getValueAt(a, 0).toString());
                if (kms.sua(manv, km) > 0) {
                    JOptionPane.showMessageDialog(this, "Sửa thành công", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                    fillTable(kms.getAll()); // Làm mới bảng
                } else {
                    JOptionPane.showMessageDialog(this, "Sửa thất bại", "Lỗi", JOptionPane.ERROR_MESSAGE);
                }
            }
        } else {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn một hàng để sửa.", "Cảnh báo", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btneditActionPerformed


